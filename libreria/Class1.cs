namespace libreria
{
    public class Class1
    {
     
        
            private double a;
            double b;

            public Class1(double x, double y)
            {
                a = x;
                b = y;


            }

            public double suma()
            {
                return a + b;
            }

            public double resta()
            {
                return a - b;
            }

            public double multi()
            {
                return a * b;
            }


            public double div()
            {
                return a / b;
            }

     }

  
}
